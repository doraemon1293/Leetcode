class Trie(object):
    END = '/'

    def __init__(self):
        self.root = {}

    def add(self, word):
        node = self.root
        for c in word:
            node = node.setdefault(c, {})
        node[self.END] = None

    def find(self, word):
        node = self.root
        for c in word:
            if c not in node:
                return False
            node = node[c]
        return self.END in node

    def find_prefix(self, word):
        node = self.root
        res = []
        i = 0
        while i < len(word) and node != None:
            node = node.get(word[i], None)
            if node != None:
                res.append(word[i])
                if self.END in node:
                    return "".join(res)
            i += 1
        return word


class Solution(object):

    def replaceWords(self, dict, sentence):
        """
        :type dict: List[str]
        :type sentence: str
        :rtype: str
        """
        trie = Trie()
        for word in dict:
            trie.add(word)
        words = sentence.split(" ")
        ans = []
        for word in words:
#             print word
#             print trie.find_prefix(word)
            ans.append(trie.find_prefix(word))
        return " ".join(ans)


