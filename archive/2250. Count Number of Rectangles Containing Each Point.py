class Solution:
    def countRectangles(self, rectangles: List[List[int]], points: List[List[int]]) -> List[int]:
        arrs = [[] for _ in range(101)]
        for l, h in rectangles:
            arrs[h].append(l)
        for arr in arrs:
            arr.sort()
        print(arrs)

        ans = []
        for x, y in points:
            ans.append(sum([len(arrs[y0])-bisect.bisect_left(arrs[y0],x) for y0 in range(y,101)]))
        return ans

